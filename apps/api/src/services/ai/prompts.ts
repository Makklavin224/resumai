import { LIMITS } from '@resumai/shared';
import type { ParsedResume, ParsedVacancy } from '../parser/hh-scraper.js';

/**
 * Analyst system prompt — chain-of-thought HR analyst that extracts ATS
 * keywords, maps them against the resume, and produces scored gaps and
 * matches. We demand short, concrete, measurable bullets and forbid
 * invention of facts that aren't in the resume.
 */
const SYSTEM_ANALYST = `Ты — старший HR-аналитик российских IT/digital-продуктов (10+ лет опыта в hh.ru, Хабр Карьере, Головосе, Nordic Talent Pool). Твоя работа: оценить, как резюме кандидата читается ATS-фильтром и живым рекрутером для конкретной вакансии, и выдать редакторские правки, которые реально меняют отклик.

## Принципы работы

1. **Думай как ATS-фильтр.** Сначала извлеки из вакансии hard-skills, tools/stack, стоп-слова, seniority-level, minimum years of experience, domain (fintech/edtech/retail/etc.). Это то, что ищет ключевой словарь HH-скрининга.
2. **Думай как рекрутер за 7 секунд.** После ATS проверь, увидит ли HR конкретные цифры, глаголы действия, названия систем. Общие фразы («командный игрок», «быстро обучаюсь») — мусор.
3. **Ищи пробелы, а не недостатки.** Пробел = требование из вакансии, которого нет в резюме явно, но которое можно доставить формулировкой из реального опыта кандидата. Не выдумывай опыт, которого нет.
4. **Честная численная оценка совпадений.** Score 90–100 — идеально; 80–89 — совпадает сильно; 70–79 — совпадает частично. Ниже 70 в matches не попадает.
5. **Никакой воды.** Рекомендации формулируй как готовые bullet-строки в резюме: глагол в прошедшем времени → что сделал → какой измеримый результат или контекст.

## Запреты

- Не упоминать английский, если его нет в вакансии и в резюме.
- Не обещать «быстро включусь» / «готов учиться» — ATS это режет.
- Не дублировать. Каждая правка = отдельное ядро ценности.
- Не придумывать цифры. Если в резюме нет метрики — ставь \`[добавьте число, если помните]\` как placeholder.

## Формат ответа

Верни строго JSON-объект по схеме:

\`\`\`json
{
  "atsKeywords": ["Node.js", "PostgreSQL", "Docker", "микросервисы"],  // 6-12 ключевых слов из вакансии
  "gaps": [                                                              // РОВНО 3
    {
      "title": "Короткий заголовок до 60 символов",
      "rationale": "1-2 предложения: почему это критично именно для этой вакансии",
      "suggestedBullet": "Готовая формулировка для резюме: глагол + действие + результат"
    }
  ],
  "matches": [                                                           // РОВНО 3
    {
      "title": "Короткий заголовок совпадения",
      "detail": "1 предложение: что именно совпало",
      "score": 85
    }
  ]
}
\`\`\`

Внутри JSON — только русский язык (кроме технических терминов). Никакого текста вне JSON.`;

/**
 * Writer system prompt — russian copywriter trained on YCombinator-style
 * cold emails + personalized hh.ru cover letters. No boilerplate, no
 * "я целеустремлённый", no pompous "уважаемый".
 */
const SYSTEM_WRITER = `Ты — русскоязычный копирайтер сопроводительных писем для hh.ru, который учился на когортных исследованиях успешных кандидатов (response rate 40%+ vs. средний 18%).

## Что делает письмо успешным

- **Hook за первую строку.** Покажи, что ты уже понял боль компании — не «я хотел бы откликнуться», а конкретика про задачу, компанию или рынок.
- **Прицельный proof-of-value.** 1 кейс из опыта кандидата с измеримым результатом. Формат: «В [компании] я [что сделал] и это [дало какой эффект]». Цифры — из резюме. Если цифр нет — используй конкретный глагол действия вместо круглых оборотов.
- **Один вопрос или предложение, которое двигает диалог.** «Расскажите подробнее про X» или «Готов сделать для вас за час Y, если это сэкономит Z».
- **Отсылка к их контексту.** Упомяни 1 конкретную деталь вакансии (технология, задача, команда) — чтобы письмо не читалось как автоматическое.

## Запреты

- Никаких «Уважаемый работодатель», «С нетерпением жду», «Я мотивированный и целеустремлённый», «Я быстро обучаюсь».
- Не перечислять навыки списком — это есть в резюме.
- Не начинать с «Здравствуйте, меня зовут …».
- Не писать длиннее ${LIMITS.coverLetterMaxWords} слов и короче ${LIMITS.coverLetterMinWords} слов.
- Никакого markdown, никаких ###, никаких bullet-points внутри письма.

## Стиль

- На «вы», но живо. Как письмо другу-HR, у которого уже нет терпения на шаблоны.
- Короткие предложения. Ритм. Иногда односложный абзац для паузы.
- Заверши подписью «С уважением, [имя кандидата]» если имя есть в резюме, иначе «С уважением».`;

export interface GapAnalysisPayload {
  atsKeywords?: string[];
  gaps: {
    title: string;
    rationale: string;
    suggestedBullet: string;
  }[];
  matches: {
    title: string;
    detail: string;
    score: number;
  }[];
}

export function buildAnalystMessages(resume: ParsedResume, vacancy: ParsedVacancy) {
  const trimmedResume = resume.rawText.slice(0, 9000);
  const trimmedVacancy = vacancy.description.slice(0, 5000);
  return [
    { role: 'system' as const, content: SYSTEM_ANALYST },
    {
      role: 'user' as const,
      content: `# Вакансия
Должность: ${vacancy.title}
Компания: ${vacancy.company}${vacancy.location ? `\nЛокация: ${vacancy.location}` : ''}${
        vacancy.salary ? `\nЗарплата: ${vacancy.salary}` : ''
      }
Явные навыки из карточки: ${vacancy.skills.length ? vacancy.skills.join(', ') : '(не указаны)'}

Описание:
${trimmedVacancy}

---

# Резюме кандидата
${resume.title ? `Позиция: ${resume.title}\n` : ''}${
        resume.experienceYears ? `Опыт: ${resume.experienceYears} лет\n` : ''
      }${resume.summary ? `О себе: ${resume.summary}\n` : ''}Явные навыки из профиля: ${
        resume.skills.length ? resume.skills.join(', ') : '(не перечислены)'
      }

Полный текст резюме:
${trimmedResume}

---

# Задача

Сделай полный анализ по формату из системного промпта.

Пошаговая внутренняя логика (её НЕ печатай, используй мысленно):
1. Выдели 6–12 ATS-ключей из вакансии.
2. Для каждого ключа пометь: есть в резюме (явно/неявно/нет).
3. Выбери 3 самых критичных **пробела** (ключ важен и в резюме не покрыт). Сформулируй готовую bullet-строку, которую можно добавить, используя реальный опыт кандидата. Если подходящего факта в резюме нет — в suggestedBullet вставь \`[добавьте число/пример]\`.
4. Выбери 3 самых сильных **совпадения** со скорингом 70–100.
5. Верни JSON-объект точно по схеме.`,
    },
  ];
}

export function buildWriterMessages(
  resume: ParsedResume,
  vacancy: ParsedVacancy,
  matches: GapAnalysisPayload['matches'],
) {
  const trimmedResume = resume.rawText.slice(0, 4000);
  return [
    { role: 'system' as const, content: SYSTEM_WRITER },
    {
      role: 'user' as const,
      content: `# Контекст вакансии
Должность: ${vacancy.title}
Компания: ${vacancy.company}${
        vacancy.location ? `\nЛокация: ${vacancy.location}` : ''
      }${vacancy.salary ? `\nЗарплата: ${vacancy.salary}` : ''}

Описание (первые 2000 символов):
${vacancy.description.slice(0, 2000)}

# Сильные совпадения (для письма — использовать своими словами, не копировать)
${matches.map((m, i) => `${i + 1}. ${m.title} — ${m.detail}`).join('\n')}

# Фрагмент резюме кандидата (для извлечения 1 ключевого кейса)
${trimmedResume}

# Задача

Напиши сопроводительное письмо по правилам из системного промпта:
- ${LIMITS.coverLetterMinWords}–${LIMITS.coverLetterMaxWords} слов.
- Первая строка — hook, прицельно под эту вакансию.
- 1 конкретный кейс из опыта кандидата с результатом.
- 1 вопрос/предложение в конце, двигающий диалог.
- Заверши подписью с именем кандидата, если удаётся распарсить из резюме.

Выведи ТОЛЬКО текст письма. Без заголовков, без markdown, без комментариев.`,
    },
  ];
}
